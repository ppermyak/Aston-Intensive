package ru.aston.teamwork.output;
/**
 * Интерфейс для вывода текстовых сообщений.
 * Позволяет абстрагировать способ вывода (консоль, файл, лог и т. д.).
 *
 * <p>Реализации этого интерфейса должны предоставлять конкретный способ вывода сообщения,
 * например, в консоль ({@link ConsoleOutput}) или в файл.
 *
 * @see ConsoleOutput
 */
public interface Output {
    /**
     * Выводит переданное сообщение.
     *
     * @param message сообщение для вывода (не должно быть {@code null})
     */
    void println(String message);
}
